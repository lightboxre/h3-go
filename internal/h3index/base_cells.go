// IsBaseCellPentagon returns true if the given base cell is a pentagon.
// There are 12 pentagon base cells in the H3 system.
func IsBaseCellPentagon(bc int) bool {
	if bc < 0 || bc >= constants.NumBaseCells {
		return false
	}
	for _, pbc := range constants.PentagonBaseCells {
		if bc == pbc {
			return true
		}
	}
	return false
}

// BaseCellNeighbor returns the neighboring base cell in the given direction.
// Returns INVALID_BASE_CELL (127) if no neighbor exists (e.g., pentagon edge).
func BaseCellNeighbor(bc, dir int) int {
	if bc < 0 || bc >= constants.NumBaseCells || dir < 0 || dir >= 7 {
		return INVALID_BASE_CELL
	}
	return baseCellNeighbors[bc][dir]
}

// BaseCellNeighborRotations returns the number of 60° CCW rotations
// when traversing from base cell bc to its neighbor in direction dir.
// Returns -1 for invalid pentagon edges.
func BaseCellNeighborRotations(bc, dir int) int {
	if bc < 0 || bc >= constants.NumBaseCells || dir < 0 || dir >= 7 {
		return -1
	}
	return baseCellNeighbor60CCWRots[bc][dir]
}
